    private void btnRenewMembershipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenewMembershipActionPerformed
        if (cbxMembership.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de membresía");
            return;
        }

        String selectedMembershipName = cbxMembership.getSelectedItem().toString();
        MembershipType membershipType = null;
        String paymentMethod = cbxPaymentMethod.getSelectedItem().toString();
        

        for (MembershipType type : MembershipType.values()) {
            if (type.getName().equals(selectedMembershipName)) {
                membershipType = type;
                break;
            }
        }

        Membership membership = new Membership(membershipType, user, paymentMethod);
        controller.renewMembership(customer.getCedula(), membership);

        JOptionPane.showMessageDialog(null, "Membresía renovada correctamente");
        view.fillCustomersTable();
        this.dispose();
    }//GEN-LAST:event_btnRenewMembershipActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (validateEmptyFields()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
            return;
        }

        LocalDate startDate = LocalDate.parse(txtStartDate.getText());
        LocalDate endDate = LocalDate.parse(txtEndDate.getText());
        String selectedMembershipName = cbxMembership.getSelectedItem().toString();
        MembershipType membershipType = null;
        String paymentMethod = cbxPaymentMethod.getSelectedItem().toString();

        for (MembershipType type : MembershipType.values()) {
            if (type.getName().equals(selectedMembershipName)) {
                membershipType = type;
                break;
            }
        }

        Membership membership = new Membership(membershipType, startDate, endDate, user, paymentMethod);
        controller.editMembership(customer.getCedula(), membership);

        JOptionPane.showMessageDialog(null, "Membresía editada");
        view.fillCustomersTable();
        this.dispose();

    }//GEN-LAST:event_btnEditActionPerformed


