    private void showMembershipData() {
        txtStartDate.setText(String.valueOf(customer.getMembership().getStartDate()));
        txtEndDate.setText(String.valueOf(customer.getMembership().getEndDate()));
        cbxPaymentMethod.setSelectedItem(customer.getMembership().getPaymentMethod());

        MembershipType customerMembershipType = customer.getMembership().getType();

        for (int i = 0; i < cbxMembership.getItemCount(); i++) {
            if (cbxMembership.getItemAt(i).equals(customerMembershipType.getName())) {
                cbxMembership.setSelectedIndex(i);
                break;
            }
        }
    }

    private void setCbxMembership() {
        for (MembershipType type : MembershipType.values()) {
            cbxMembership.addItem(type.getName());
        }
    }

    private boolean validateEmptyFields() {
        return (txtStartDate.getText().isEmpty() || txtEndDate.getText().isEmpty() || cbxMembership.getSelectedIndex() == 0
                || cbxPaymentMethod.getSelectedIndex() == 0);
    }

