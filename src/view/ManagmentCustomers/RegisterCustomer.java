    private void validateFields() {
        String cedula = txtCedula.getText().trim();
        Customer customer = controller.selectCustomer(cedula);

        boolean enableBtnRegisterCustomer = true;

        if (!cedula.isEmpty() && customer != null) {
            lblAlert.setText("Cédula ya registrada");
            showAlert();
            enableBtnRegisterCustomer = false;
        } else {
            hideAlert();
        }

        btnRegisterCustomer.setEnabled(enableBtnRegisterCustomer);
    }

