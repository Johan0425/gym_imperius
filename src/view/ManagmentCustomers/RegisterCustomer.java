    private void cbxMembershipItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxMembershipItemStateChanged
        String selectedMembershipName = cbxMembership.getSelectedItem().toString();
        MembershipType membershipType = null;

        for (MembershipType type : MembershipType.values()) {
            if (type.getName().equals(selectedMembershipName)) {
                membershipType = type;
                break;
            }
        }

        if (membershipType == MembershipType.DIA) {
            noEnabledFields();
        } else {
            enabledFields();
        }

    }//GEN-LAST:event_cbxMembershipItemStateChanged


