    private void btnRegisterCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterCustomerActionPerformed
        String selectedMembershipName = cbxMembership.getSelectedItem().toString();
        MembershipType membershipType = getMembershipType(selectedMembershipName);

        if (membershipType == null) {
            JOptionPane.showMessageDialog(null, "Escoja un tipo de membresía");
            return;
        }

        // Validate input fields
        if (hasEmptyFields()) {
            String alertMessage = "";
            if (membershipType != MembershipType.DIA) {
                alertMessage = "Ingrese todos los datos";
            } else if (hasEmptyFieldsCustomerDay()) {
                alertMessage = "Ingrese todos los datos para la clase";
            }

            if (!alertMessage.isEmpty()) {
                lblAlert.setText(alertMessage);
                showAlert();
                return;
            }
        }

// Resto del código después de la validación de campos vacíos
// Emergency contact data
        String contactFullname = txtContactFullname.getText();
        String contactPhoneNumber = txtContactPhoneNumber.getText().trim();

        EmergencyContact emergencyContact = new EmergencyContact(contactFullname, contactPhoneNumber);

// Membership data
        String paymentMethod = cbxPaymentMethod.getSelectedItem().toString();

// Customer data
        String cedula = txtCedula.getText().trim();
        String fullname = txtCustomerFullname.getText();
        String phoneNumber = txtCustomerPhoneNumber.getText().trim();
        String healthInformation = txtHealthInformation.getText();

// Process customer registration based on membership type
        if (membershipType == MembershipType.DIA) {
            registerCustomerForDayMembership(fullname, phoneNumber, cedula, paymentMethod);
        } else {

            LocalDate birthDate = parseDate(txtBirthDate.getText());
            registerRegularCustomer(birthDate, emergencyContact, membershipType, healthInformation, fullname, phoneNumber, cedula, paymentMethod);
        }

    }

    // Helper methods
    private MembershipType getMembershipType(String selectedMembershipName) {
        for (MembershipType type : MembershipType.values()) {
            if (type.getName().equals(selectedMembershipName)) {
                return type;
            }
        }
        return null;
    }

    private void registerCustomerForDayMembership(String fullname, String phoneNumber, String cedula, String paymentMethod) {
        Customer customerDay = new Customer(fullname, phoneNumber, cedula, new Membership(MembershipType.DIA, user, paymentMethod));
        controllerday.insertCustomerDay(customerDay);
        JOptionPane.showMessageDialog(null, "Cliente registrado correctamente");
        view.fillCustomersTable();
        this.dispose();
        viewM.openManagmentCustomersView();
    }

    private void registerRegularCustomer(LocalDate birthDate, EmergencyContact emergencyContact, MembershipType membershipType,
            String healthInformation, String fullname, String phoneNumber, String cedula, String paymentMethod) {
        Customer customer = new Customer(birthDate, emergencyContact, new Membership(membershipType, user, paymentMethod), healthInformation, fullname, phoneNumber, cedula);
        controller.insertCustomer(customer);
        JOptionPane.showMessageDialog(null, "Cliente registrado correctamente");
        view.fillCustomersTable();
        this.dispose();
        viewM.openManagmentCustomersView();

    }//GEN-LAST:event_btnRegisterCustomerActionPerformed

