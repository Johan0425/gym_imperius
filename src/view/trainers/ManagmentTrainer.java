    public final void fillTrainersTable() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        ArrayList<Object> trainers = controller.listTrainers();
        model.setColumnIdentifiers(new Object[]{
            "Cédula", "Nombre completo", "Teléfono", "Contraseña"
        });

        trainersTable.setModel(model);
        tableCustomization();

        trainersTable.setAutoCreateRowSorter(true);
        sorter = new TableRowSorter<>(model);
        trainersTable.setRowSorter(sorter);

        for (int i = 0; i < trainers.size(); i++) {
            Trainer trn = (Trainer) trainers.get(i);
            model.addRow(new Object[]{
                trn.getCedula(),
                trn.getFullname(),
                trn.getPhoneNumber(),
                trn.getPassword()
            });
        }
    }

    private void filter() {
        sorter.setRowFilter(RowFilter.regexFilter(txtFilter.getText()));
    }

