    private void btnUpdateTrainerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateTrainerActionPerformed
        int selection = trainersTable.getSelectedRow();

        if (selection < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un entrenador antes de continuar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String cedula = trainersTable.getValueAt(selection, 0).toString();
        Trainer trainer = controller.selectTrainer(cedula);

        if (trainer != null) {
            view.openUpdateTrainerView(this, trainer);
        }

    }//GEN-LAST:event_btnUpdateTrainerActionPerformed

    private void btnDeleteTrainerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteTrainerActionPerformed
        int selection = trainersTable.getSelectedRow();

        if (selection < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un entrenador antes de continuar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String cedula = trainersTable.getValueAt(selection, 0).toString();

        int answer = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el entrenador?", "Advertencia", JOptionPane.YES_NO_OPTION);

        if (answer == 0) {
            controller.deleteTrainer(cedula);
            JOptionPane.showMessageDialog(null, "Entrenador eliminado correctamente");
            fillTrainersTable();
        }
    }//GEN-LAST:event_btnDeleteTrainerActionPerformed

