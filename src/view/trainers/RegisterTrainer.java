    private void validateSeePassword() {
        if (txtPassword.getText().equals("")) {
            cbxSeePassword.setVisible(false);
        } else {
            cbxSeePassword.setVisible(true);
        }
    }

    private void showPlaceHolders() {
        new PlaceHolder("Ingrese una cédula", txtCedula);
        new PlaceHolder("Ingrese un nombre", txtFullname);
        new PlaceHolder("Ingrese un teléfono", txtPhoneNumber);
        new PlaceHolder("Ingrese una contraseña", txtPassword);
    }

    private boolean hasEmptyFields() {
        return (txtCedula.getText().isEmpty() || txtFullname.getText().isEmpty() || txtPhoneNumber.getText().length() < 12
                || txtPassword.getText().isEmpty());
    }

    private void hideAlert() {
        lblAlert.setVisible(false);
    }

    private void showAlert() {
        lblAlert.setVisible(true);
    }

    private void validateFields() {
        String cedula = txtCedula.getText().trim();
        Trainer trainer = controller.selectTrainer(cedula);

        boolean enableBtnRegisterTrainer = true;

        if (!cedula.isEmpty() && trainer != null) {
            lblAlert.setText("Cédula ya registrada");
            showAlert();
            enableBtnRegisterTrainer = false;
        } else {
            hideAlert();
        }

        btnRegisterTrainer.setEnabled(enableBtnRegisterTrainer);
    }

