    private void txtCedulaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedulaKeyTyped
        String cedula = txtCedula.getText();
        char c = evt.getKeyChar();

        if (!Character.isDigit(c) || cedula.length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCedulaKeyTyped

    private void btnRegisterTrainerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterTrainerActionPerformed
        if (hasEmptyFields()) {
            lblAlert.setText("Ingrese todos los datos");
            showAlert();
            return;
        }

        String cedula = txtCedula.getText().trim();
        String fullname = txtFullname.getText();
        String phoneNumber = txtPhoneNumber.getText().trim();
        String password = txtPassword.getText().trim();

        Trainer trainer = new Trainer(fullname, phoneNumber, cedula, password);
        controller.insertTrainer(trainer);
        JOptionPane.showMessageDialog(null, "Entrenador registrado correctamente");
        view.fillTrainersTable();

        this.dispose();
    }//GEN-LAST:event_btnRegisterTrainerActionPerformed

